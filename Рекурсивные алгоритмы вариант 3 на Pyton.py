def gcd(a, b):
    """
    Находит наибольший общий делитель (НОД) двух чисел с помощью рекурсивного алгоритма Евклида.
    
    Параметры:
    a (int): первое число
    b (int): второе число
    
    Возвращает:
    int: НОД чисел a и b
    """
    # Базовый случай: если второе число равно 0, НОД равен первому числу
    if b == 0:
        return a
    # Рекурсивный случай: НОД(a, b) = НОД(b, a mod b)
    else:
        return gcd(b, a % b)

# Пример использования
if __name__ == "__main__":
    num1 = 48
    num2 = 18
    result = gcd(num1, num2)
    print(f"НОД({num1}, {num2}) = {result}")
