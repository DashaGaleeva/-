def insertion_sort(arr):
    # Длина массива
    n = len(arr)
    
    # Проходим по каждому элементу массива, начиная со второго
    for i in range(1, n):
        key = arr[i]  # Текущий элемент, который нужно вставить на правильную позицию
        
        # Индекс предыдущего элемента
        j = i - 1
        
        # Смещаемся назад по уже отсортированному участку массива
        while j >= 0 and arr[j] > key:
            # Если текущий элемент больше ключа, сдвигаем его вправо
            arr[j + 1] = arr[j]
            j -= 1  # Переходим к предыдущему элементу
        
        # Вставляем ключ на освободившееся место
        arr[j + 1] = key


# Тестовый массив
array = [12, 11, 13, 5, 6]

# Вызываем функцию сортировки
insertion_sort(array)

# Выводим отсортированный массив
print("Отсортированный массив:", array)
