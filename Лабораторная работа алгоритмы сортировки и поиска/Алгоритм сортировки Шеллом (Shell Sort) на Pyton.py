def shell_sort(arr):
    # Начнём с большой разницы между элементами (gap)
    gap = len(arr) // 2
    
    # Постепенно уменьшаем разницу (gap), пока не достигнем нуля
    while gap > 0:
        # Пробегаем по массиву, начиная с gap-го элемента
        for i in range(gap, len(arr)):
            temp = arr[i]  # Запоминаем текущий элемент
            j = i
            
            # Продвигаем элемент назад, пока соблюдается условие упорядоченности
            while j >= gap and arr[j-gap] > temp:
                arr[j] = arr[j-gap]  # Перемещение большего элемента вперёд
                j -= gap
            
            # Ставим элемент на его место
            arr[j] = temp
        
        # Уменьшаем промежуток (gap) вдвое
        gap //= 2

# Пример использования
data = [64, 34, 25, 12, 22, 11, 90]
shell_sort(data)
print("Отсортированный массив:", data)
