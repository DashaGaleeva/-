def binary_search(arr, target):
    """
    Осуществляет бинарный поиск элемента 'target' в отсортированном списке 'arr'
    Возвращает индекс элемента, если он найден, иначе -1.
    """
    low = 0                     # Нижняя граница поиска
    high = len(arr) - 1         # Верхняя граница поиска

    while low <= high:          # Пока нижняя граница не пересеклась с верхней
        mid = (low + high) // 2 # Находим середину отрезка
        
        # Если элемент найден, возвращаем его индекс
        if arr[mid] == target:
            return mid
        
        # Если цель меньше среднего элемента, сужаем поиск влево
        elif arr[mid] > target:
            high = mid - 1
        
        # Если цель больше среднего элемента, сужаем поиск вправо
        else:
            low = mid + 1
    
    # Если элемент не найден, возвращаем -1
    return -1

# Пример использования
my_list = [1, 3, 5, 7, 9, 11, 13, 15]
search_element = 9

# Вызов функции поиска
index = binary_search(my_list, search_element)

# Вывод результата
if index != -1:
    print(f"Элемент {search_element} найден на индексе {index}")
else:
    print(f"Элемент {search_element} не найден в списке.")
